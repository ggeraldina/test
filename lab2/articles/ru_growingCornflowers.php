        <h2>Выращивание в саду</h2> 
        
        <p>
            Для этого растения больше всего подходит открытое место с
            хорошим солнечным освещением. Оно любит свободное пространство,
            поэтому лучше рассадить кусты на расстояние 20-50 см. Это даст
            возможность сформироваться и развиться красивому васильку. 
            Если растение живет в цветнике, то лучшей для него будет
            южная сторона, так как она самая освещаемая. Большинство
            видов будут рады богатой почве, но есть сорта, которые 
            предпочитают щелочной грунт. Поливать нужно умеренно. 
            Василек легче перенесет засуху, чем чрезмерное увлажнение
        </p>
        
        <h2>Уход за васильками</h2>
        
        <p>
           Вся прелесть этого растения в неприхотливости в уходе 
           за ним. Ему требуется лишь периодическое рыхление грунта 
           (один-два раза в месяц), умеренный полив и удаление сорняков. 
           Если вы хотите, чтобы василек продолжал цвести большее 
           количество времени, следует обрывать отцветшие корзинки. 
           Если растение выращивается на продажу, то во время полива
           следует вносить специальный комплекс удобрений. Болезни и
           вредители практически не поражают васильки. Изредка он болеет
           фузариозом, а листья поражаются паутинным клещом 
        </p>
        
