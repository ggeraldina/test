        <h1>Выращивание и уход</h1>
        
        <h2>Посадка</h2>
        
        <p>
            Весна является самой благоприятной порой для посадки васильков. 
            Почва должна прогреться. Это происходит с конца апреля. Почва
            для василька подойдет любая, даже если в ней содержится известь.
            Для этого ее нужно взрыхлить и рассаживать цветки на расстоянии 
            не меньше 50 см друг от друга. Это даст возможность кусту полноценно
            развиться. Грунт после засыпания лунки нужно слегка притоптать. 
            Такой прием сможет легко ликвидировать образовавшиеся полости,
            а растение благодаря этому быстрее укоренится. Васильки любят 
            солнце, поэтому если высадить их в тень, они будут плохо
            развиваться и цвести
        </p>    
        
        <h2>Размножение</h2>
        
        <p>
            Существует несколько способов разведения васильков. 
            Это можно сделать с помощью корневых черенков, семенами
            или делением куста. В период цветения растение и само активно 
            осуществляет самосев. Пересадку можно проводить только после
            окончания этого периода. Для этого нужно выкопать выбранный 
            куст, убрать всю землю с корней и омыть их. Наземные побеги
            обрезаются и от куста отделяют периферийную часть. Она
            должна содержать не меньше 3 почек следующего года. Затем 
            ее сразу же высаживают обратно в землю. Наземная часть 
            обрезается так, чтобы ее высота не превышала 10 см, полив 
            должен быть умеренным: 3-4 раз в неделю будет достаточно.
            Уже в будущем году новое растение будет ярко цвести. Если
            же нужно размножить однолетний василек, то для этого подойдут
            только семена. Они высаживаются в начале мая. Пересаживать
            их не рекомендуется
        </p>
         
